def Seizoen(maand):
    if maand == "januari":
        seizoen = "Winter"
    elif maand == "februari":
        seizoen = "Winter"
    elif maand == "maart":
        seizoen = "Lente"
    elif maand == "april":
        seizoen = "Lente"
    elif maand == "mei":
        seizoen = "Lente"
    elif maand == "juni":
        seizoen = "Zomer"
    elif maand == "juli":
        seizoen = "Zomer"
    elif maand == "augustus":
        seizoen = "Zomer"
    elif maand == "september":
        seizoen = "Herfst"
    elif maand == "oktober":
        seizoen = "Herfst"
    elif maand == "november":
        seizoen = "Herfst"
    elif maand == "december":
        seizoen = "Winter"
    else:
        seizoen = "Ongeldige maand"
    return seizoen