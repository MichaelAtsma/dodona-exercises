def Seizoen(maand):
    if maand == "Januari":
        seizoen = "Winter"
    elif maand == "Februari":
        seizoen = "Winter"
    elif maand == "Maart":
        seizoen = "Lente"
    elif maand == "April":
        seizoen = "Lente"
    elif maand == "Mei":
        seizoen = "Lente"
    elif maand == "Juni":
        seizoen = "Zomer"
    elif maand == "Juli":
        seizoen = "Zomer"
    elif maand == "Augustus":
        seizoen = "Zomer"
    elif maand == "September":
        seizoen = "Herfst"
    elif maand == "Oktober":
        seizoen = "Herfst"
    elif maand == "November":
        seizoen = "Herfst"
    elif maand == "December":
        seizoen = "Winter"
    else:
        seizoen = "Ongeldige maand"
    return seizoen